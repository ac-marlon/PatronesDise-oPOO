package parqueaderobuilder;

public class Nivel {

    private String bahia = "";
    private String camara = "";
    private String salidaEmergencia = "";

    public String getBahia() {
        return bahia;
    }

    public void setBahia(String bahia) {
        this.bahia = bahia;
    }

    public String getCamara() {
        return camara;
    }

    public void setCamara(String camara) {
        this.camara = camara;
    }

    public String getSalidaEmergencia() {
        return salidaEmergencia;
    }

    public void setSalidaEmergencia(String salidaEmergencia) {
        this.salidaEmergencia = salidaEmergencia;
    }

}
